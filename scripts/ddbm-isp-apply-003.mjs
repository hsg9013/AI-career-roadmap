async function main() {
  if (!MEMBER_ID || !MEMBER_PW) { console.error('❌ DDBM_MEMBER_ID/DDBM_MEMBER_PW 필요'); process.exit(1); }
  console.log(`모드: ${APPLY ? '🟢 APPLY (실제 반영)' : '🔵 DRY_RUN (읽기 전용)'}  대상: #${TARGET}`);

  console.log('🔐 로그인...');
  await call('POST', '/auth/member/login', { member_id: MEMBER_ID, password: MEMBER_PW });

  // 안전 확인: 대상(#60)과 스냅샷(#85) 제목 표시
  for (const id of [TARGET, '85']) {
    try {
      const r = await call('GET', `/canvases/${id}`);
      const c = r?.data || r;
      console.log(`   #${id}: "${c.title}"  primary=${c.is_primary}`);
    } catch (e) { console.log(`   #${id}: 조회 실패 (${e.message})`); }
  }

  const keys = Object.keys(BLOCKS);
  console.log(`\n반영 대상 ${keys.length}개 블록:`);
  for (const k of keys) console.log(`   • ${k} — ${BLOCKS[k].length}자`);

  if (!APPLY) {
    console.log('\n(DRY_RUN) 실제 반영하려면 APPLY=1 을 붙여 다시 실행하세요.');
    return;
  }

  console.log(`\n✍️  #${TARGET} 반영 시작...`);
  let n = 0;
  for (const k of keys) {
    await call('PUT', `/canvases/${TARGET}/blocks/${k}`, { content: BLOCKS[k] });
    console.log(`   ✅ ${k} (${BLOCKS[k].length}자)`);
    n++;
  }
  console.log(`\n완료 — #${TARGET} 에 ${n}/${keys.length} 블록 반영됨.`);
}

main().catch((e) => { console.error('💥', e.message); process.exit(1); });
